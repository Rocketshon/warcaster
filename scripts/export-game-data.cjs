/**
 * Export Game Data — reads the compiled JS data and writes JSON files.
 *
 * Approach: Use regex to extract the JSON objects from the TS source files
 * since they're essentially JSON with type annotations stripped.
 *
 * Usage: node scripts/export-game-data.cjs
 */

const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const outDir = path.resolve(projectRoot, 'dist-data');
const gameDir = path.resolve(outDir, 'wh40k-10e');

function extractObject(content, varName) {
  // Find the variable declaration and extract everything after the =
  const patterns = [
    new RegExp(`export\\s+const\\s+${varName}[^=]*=\\s*`),
    new RegExp(`const\\s+${varName}[^=]*=\\s*`),
  ];

  let startIdx = -1;
  for (const pat of patterns) {
    const match = content.match(pat);
    if (match && match.index !== undefined) {
      startIdx = match.index + match[0].length;
      break;
    }
  }
  if (startIdx === -1) return null;

  // Find the matching closing bracket
  let depth = 0;
  let inString = false;
  let stringChar = '';
  let end = startIdx;

  for (let i = startIdx; i < content.length; i++) {
    const c = content[i];

    if (inString) {
      if (c === '\\') { i++; continue; }
      if (c === stringChar) inString = false;
      continue;
    }

    if (c === '"' || c === "'" || c === '`') {
      inString = true;
      stringChar = c;
      continue;
    }

    if (c === '{' || c === '[') depth++;
    if (c === '}' || c === ']') {
      depth--;
      if (depth === 0) {
        end = i + 1;
        break;
      }
    }

    // Handle 'null' at top level
    if (depth === 0 && content.slice(i, i + 4) === 'null') {
      return null;
    }
  }

  const raw = content.slice(startIdx, end);

  // The content is already valid JSON (generated by build-data.ts)
  try {
    return JSON.parse(raw);
  } catch {
    // Try stripping trailing semicolons
    try {
      return JSON.parse(raw.replace(/;\s*$/, ''));
    } catch (e) {
      console.error(`Failed to parse ${varName}: ${e.message}`);
      console.error(`First 200 chars: ${raw.slice(0, 200)}`);
      return null;
    }
  }
}

function main() {
  console.log('Exporting game data...\n');

  // Create output directories
  fs.mkdirSync(gameDir, { recursive: true });

  // Read source files
  const unitsContent = fs.readFileSync(path.resolve(projectRoot, 'src/data/units.ts'), 'utf-8');
  const rulesContent = fs.readFileSync(path.resolve(projectRoot, 'src/data/rules.ts'), 'utf-8');
  const generalContent = fs.readFileSync(path.resolve(projectRoot, 'src/data/general.ts'), 'utf-8');

  // Extract data objects
  console.log('Parsing units...');
  const units = extractObject(unitsContent, '_units') || extractObject(unitsContent, 'UNITS');
  if (!units) { console.error('FATAL: Could not extract UNITS'); process.exit(1); }

  console.log('Parsing rules...');
  const rules = extractObject(rulesContent, '_factionRules') || extractObject(rulesContent, 'FACTION_RULES');
  if (!rules) { console.error('FATAL: Could not extract FACTION_RULES'); process.exit(1); }

  console.log('Parsing general...');
  const coreRules = extractObject(generalContent, 'CORE_RULES');
  const crusadeRules = extractObject(generalContent, 'CRUSADE_RULES');
  const rulesCommentary = extractObject(generalContent, 'RULES_COMMENTARY');

  const general = { coreRules, crusadeRules, rulesCommentary };

  // Write JSON files
  console.log('\nWriting JSON files...');

  const unitsJson = JSON.stringify(units);
  fs.writeFileSync(path.resolve(gameDir, 'units.json'), unitsJson);
  console.log(`  units.json: ${(unitsJson.length / 1024).toFixed(0)} KB`);

  const rulesJson = JSON.stringify(rules);
  fs.writeFileSync(path.resolve(gameDir, 'rules.json'), rulesJson);
  console.log(`  rules.json: ${(rulesJson.length / 1024).toFixed(0)} KB`);

  const generalJson = JSON.stringify(general);
  fs.writeFileSync(path.resolve(gameDir, 'general.json'), generalJson);
  console.log(`  general.json: ${(generalJson.length / 1024).toFixed(0)} KB`);

  // Write manifest
  const manifest = {
    version: '1.0.0',
    updated: new Date().toISOString(),
    games: [
      {
        id: 'wh40k-10e',
        name: 'Warhammer 40,000',
        edition: '10th Edition',
        icon: '\u2694\uFE0F',
        version: '2026.03.30',
        description: '26 factions, 1400+ datasheets, full rules and stratagems',
        files: {
          units: 'https://raw.githubusercontent.com/Rocketshon/warcaster-data/main/wh40k-10e/units.json',
          rules: 'https://raw.githubusercontent.com/Rocketshon/warcaster-data/main/wh40k-10e/rules.json',
          general: 'https://raw.githubusercontent.com/Rocketshon/warcaster-data/main/wh40k-10e/general.json',
        },
      },
    ],
  };

  fs.writeFileSync(path.resolve(outDir, 'manifest.json'), JSON.stringify(manifest, null, 2));
  console.log('  manifest.json: written');

  console.log(`\nDone! Output: ${outDir}`);
  console.log(`\nTotal: ${((unitsJson.length + rulesJson.length + generalJson.length) / 1024 / 1024).toFixed(1)} MB`);
}

main();
